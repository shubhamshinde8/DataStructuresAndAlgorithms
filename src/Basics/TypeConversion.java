package Basics;

public class TypeConversion {
    public static void main(String[] args) {

        long a=90;
        int b= (int) a;
        System.out.println(b);

        double x=89;
        long y=(long) x;

        System.out.println(y);

        byte a1=12;
        int b1 = a1;

        System.out.println(b1);



    }
}
